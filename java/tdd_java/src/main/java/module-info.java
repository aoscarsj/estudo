module tdd.java {
    // Dicas
    /*
    * CTRL + SHIFT + T -> abrir menu de criação de testes.
    *   Abre a classe que quer testar e executa o atalho.
    *
    *
    * TDD - Teste Driven Developen
    *
    * Faz o teste -> Implementa -> Refatora -> faz o próximo teste.
    * No geral, se testa
    *   O valor devolvido pelo método
    *   O objeto modificado pelo método
    *   A exception lançada pelo método
    *
    *
    * Em um projeto, nao se testa todas as classes e metodos, eh improdutivo.
    * Voce nao testa construtores ou getters e setters, teste classes com regras de negocio
    *   com infos importantes, coisas que tendem a mudar no futuro.
    * */
}